class MyClass:
    def add(self):
        print("addition of two numbers")
        return 2 + 3
    def sub(self):
        print("subtraction of two numbers")
        return 5 - 3
    def mul(self):
        print("multiplication of two numbers")
        return 2 * 3
    def div(self):
        print("division of two numbers")
        return 6 / 3
x = MyClass()
print(x.add())
print(x.sub())
print(x.mul())
print(x.div())


